    private void InserirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_InserirMouseClicked
                                  
        String nome = nome1.getText();
        String sobrenome = sobrenome1.getText();
        String RG = RG1.getText();
        String CPF = CPF1.getText();
        String endereco = endereco1.getText();
        Cliente cliente = new Cliente(-1L,nome,sobrenome,RG,CPF, endereco, 100);
        listaDeClientes.add(cliente);
        modelo.adicionaCliente(cliente);
        
        JFrame jFrame = new JFrame();
        JOptionPane.showMessageDialog(jFrame, "Cliente Cadastrado com sucesso!");
    }//GEN-LAST:event_InserirMouseClicked

