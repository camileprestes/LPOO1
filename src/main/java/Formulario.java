    private void AlterarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AlterarMouseClicked
        if(linhaClicada!=-1){
            Cliente cliente = modelo.getCliente(linhaClicada);

            cliente.setNome(nome1.getText());
            cliente.setSobrenome(sobrenome1.getText());
            cliente.setRG(RG1.getText());
            cliente.setCPF(CPF1.getText());
            cliente.setEndereco(endereco1.getText());
            //Atualiza tabela
            modelo.fireTableRowsUpdated(linhaClicada, linhaClicada);
            
        }
    }//GEN-LAST:event_AlterarMouseClicked

    private void ExcluirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ExcluirMouseClicked
         int[] linhasSelecionadas = tabela1.getSelectedRows();
        List<Cliente> listaExcluir = new ArrayList();
        for (int i = 0; i < linhasSelecionadas.length; i++) {
            Cliente cliente = modelo.getCliente(linhasSelecionadas[i]);
            listaExcluir.add(cliente);

        }
        for(Cliente cliente:listaExcluir){
            listaDeClientes.remove(cliente);
            modelo.removeCliente(cliente);
        }
    }//GEN-LAST:event_ExcluirMouseClicked

    private void tabela1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela1MouseClicked
        //Pega a linha clicada
        linhaClicada = tabela1.rowAtPoint(evt.getPoint());
        //Pega o contato da linha clidada
        Cliente cliente = modelo.getCliente(linhaClicada);
        //Seta os dados nos componentes
        nome1.setText(cliente.getNome());
        sobrenome1.setText(cliente.getSobrenome());
        RG1.setText(cliente.getRG());
        CPF1.setText(cliente.getCPF());
        endereco1.setText(cliente.getEndereco());
    }//GEN-LAST:event_tabela1MouseClicked

